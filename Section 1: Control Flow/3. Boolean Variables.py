# Boolean Variables
# True and False are the only bool types.
# Any variable that is assigned one of these values is called a boolean variable.

# Boolean variables can be created by assigning the True or False to a variable.
# Example (bool)
my_baby_bool = True
# This will check if it is a boolean variable
type(my_baby_bool)
# This will print the results.
print(type(my_baby_bool))

# This example will show what is not a boolean variable.
# Example (str)
my_baby_bool_two = "true"
# This will check if it is a boolean variable
type(my_baby_bool_two)
# Prints the results.
print(type(my_baby_bool_two))

print("Essentially a boolean variable can either be True or False and defined like such.")