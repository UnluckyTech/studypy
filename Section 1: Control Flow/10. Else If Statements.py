# Else If Statements
# An Else If Statement or "elif" checks another condition
# after the previous if statements conditions aren't met.

# Example
grade = 86

if grade >= 90:
  print('A')
elif grade >= 80:
  print('B')
elif grade >= 70:
  print('C')
elif grade >= 60:
  print('D')
else:
  print('F')