# Relational Operators: Equals and Not Equals
# Boolean expressions can be created by using relational operators.
# Relational Operators compare two items and return either True or False.
# This can also be called comparators.

# These operators compare two items and return True or False
# if they are equal or not.
# Equals: == and Not equals: !=

#Examples
1 == 1      # True
2 != 4      # True
3 == 5      # False
'7' == 7    # False due to '' identifying the 7 as a string instead of a integer.

#Exercise
(5 * 2) - 1 == 8 + 1    #True
13 - 6 != (3 * 2) + 1   #False
3 * (2 - 1) == 4 - 1    #True

