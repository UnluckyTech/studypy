# Accessing 2D Lists
# You can access 2D lists similar to how you do it with lists.
# Just type in additional brackets. test[2][1]
# Example
class_name_test = [['Jenny', 90], ['Alexus', 85.5], ['Sam', 83], ['Ellie', 101.5]]
print(class_name_test)
sams_score = class_name_test[2][1]
print(sams_score)
ellies_score = class_name_test[-1][-1]
print(ellies_score)