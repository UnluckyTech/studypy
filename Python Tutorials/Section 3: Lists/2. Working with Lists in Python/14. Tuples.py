# Tuples
# A data structure in python that allows us to store multiple 
# pieces of data. It's similar to a list but its immutable. This means
# once you make this list then YOU CAN NOT CHANGE THE DATA INSIDE.

my_info = ('Scott', 22, 'Programmer')
print(my_info)
print(my_info[0])

name, age, occupation = my_info

print(name)
print(age)
print(occupation)

