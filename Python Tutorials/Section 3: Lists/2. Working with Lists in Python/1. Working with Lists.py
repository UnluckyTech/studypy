# Working with Lists

# In this lesson, you'll learn how to:
# Add and remove items from a list using a specific index.
# Create lists with continuous values.
# Get the length of a list.
# Select portions of a list (called slicing).
# Count the number of times that an element appears in a list.
# Sort a list of items.

# Python List Methods
# .count() - A list method to count the number of occurrences
# of an element in a list.

# .insert() - A list method to insert an element into a
# specific index of a list.

# .pop() - A list method to remove an element from a specific
# index or from the end of a list.

# range() - A built-on Python function to create a sequence
# of integers.

# .sort() / sorted() - A method and a built-in function to sort a list.
