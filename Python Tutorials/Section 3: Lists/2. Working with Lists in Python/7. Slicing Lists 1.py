# Slicing Lists 1
# Extracts only a portion of a list.
# Example
letters = ["a", "b", "c", "d", "e", "f", "g"]
sliced_list = letters[1:6]
print(sliced_list)

suitcase = ["shirt", "shirt", "pants", "pants", "pajamas", "books"]
beginning = suitcase[0:2]
print(beginning)
middle = suitcase[2:4]
print(middle)