# Length
# The length functon allows you to see the length of a list
# or how many points are saved on a list.

# Example
long_list = [1, 5, 6, 7, -23, 69.5, True, "very", "long", "list", "that", "keeps", "going.", "Let's", "practice", "getting", "the", "length"]
range_list = range(2, 3000, 100) 
# len() is what tells the number of items in a specified list.
long_list_len = len(long_list)
print(long_list_len)
range_list_length = len(range_list)
print(range_list_length)